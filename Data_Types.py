#Strings
first_name = "Ed"
last_name = "Ward"
full_name = first_name +" "+ last_name
print("Hello "+full_name)
print(type(full_name))

#Integers
age = 21
age = age + 1
print(age)
print(type(age))
print("Your age is: "+str(age))

#Floats
height = 250.5
print(height)
print(type(height))
print("Your height is "+str(height)+" cm")

#Booleans
human = False
print(human)
print(type(human))
print("Are you a human: "+str(human))
